public class Horse {

    private String colour;
    private int height;

    Horse() {

    }

    Horse(String colour) {

        this.colour = colour;
    }

    public String getColour() {

        return this.colour;
    }

    public void setColour(String colour) {

        this.colour = colour;
    }

    public int getHeight() {

        return this.height;
    }

    public void setHeight(int height) {

        this.height = height;
    }


}
