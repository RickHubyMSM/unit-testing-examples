public class Rabbit {

    private String breed;
    private int weight;

    Rabbit() {

    }

    Rabbit(String breed) {

        this.breed = breed;
    }

    public String getBreed() {

        return this.breed;
    }

    public void setBreed(String breed) {

        this.breed = breed;
    }

    public int getWeight() {

        return this.weight;
    }

    public void setWeight(int weight) {

        this.weight = weight;
    }


}
